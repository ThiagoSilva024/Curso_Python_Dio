#Fatiamaneto de string

nome = "Thiago Henrique Da Silva"

print(nome[2])  #Acessa a string por meio da posicao 

print(nome[:6]) #Stop: A string termina na posicao 3 (OBS.: 3-1=2 o caracter da posicao 3 nao e impresso)

print(nome[7:]) #Start: Inicia a strinf pela posicao 3

print(nome[7:15])  #Sub-string: Pega um pedaco da string (OBS.: INICIO[7], FIM[15])

print(nome[7:15:2]) #Retorna a string[Henrique], uma letra a cada 2 casas [hniu]

print(nome[:]) #Retorna uma copia da string inteira

print(nome[::-1]) #Espelha string: Retorna string de tras para frente.

