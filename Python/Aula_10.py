#Operadores De Atribuicao
saldo = 500
print(saldo)

saldo = 200
print(saldo)

print("-" * 10,"OPERADORES DE ATRIBUICAO","-" * 10)
saldo += 10
print(f"ADICAO: {saldo}")

saldo -= 5
print(f"SUBTRACAO: {saldo}")

saldo //= 2
print(f"DIVISAO INTEIRO:  {saldo}")

saldo /= 2
print(f"DIVISAO: {saldo}")

saldo *= 10
print(f"MULTIPLICACAO: {saldo}")

saldo %= 4
print(f"MODULO: {saldo}")

saldo **= 2 
print(f"EXPONENCIAL: {saldo}")